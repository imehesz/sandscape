<h2>About SandScape Project</h2>
<p>The purpose of <strong>Sandscape</strong> project is to create a virtual, online and purely browser based, environment where players can meet to play a CCG. <strong>Sandscape</strong> is, as all things <em>WTactics</em>, an open source endeavor that should be generic, easy to extend and modify so that it can be of use in other projects, making it possible to play almost any CCG with it.</p>
